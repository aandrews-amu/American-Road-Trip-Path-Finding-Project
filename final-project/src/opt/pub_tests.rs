use super::*;

#[test]
fn test_tsp_1() {
    let vcount: usize = 3;
    let edges: &[(usize, usize, u32)] = &[
        (0, 1, 10),
        (0, 2, 10),
        (1, 2, 10),
        (1, 0, 10),
        (2, 0, 10),
        (2, 1, 10),
    ];

    assert_eq!(roadtrip(vcount, edges), (vec![0, 1, 2, 0], 30));
}

